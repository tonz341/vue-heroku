<script>
export default {
  name: 'contact',
  data () {
    return {
      message: ''
    }
  },
  mounted () {
    window.socket.on('global-chat:send-clients', (message) => {
      this.$store.commit('INSERT_MESSAGES', message)
    })
  },
  methods: {
    sendMessage () {
      window.socket.emit('global-chat:send-server', this.message)
      this.message = ''
    }
  }
}
</script>

